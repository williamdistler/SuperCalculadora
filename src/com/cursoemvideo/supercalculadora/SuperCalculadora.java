    private void btnClickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClickActionPerformed
        // TODO add your handling code here:
        
        int valor = Integer.parseInt(spnValor.getValue().toString());
        //Resto da divisao por 2
        int resto = valor % 2;
        lblResto.setText(Integer.toString(resto));
        //Cubo
        double cubo = Math.pow(valor, 3);
        lblCubo.setText(Double.toString(cubo));
        //Raiz Quadrada
        double raiz = Math.sqrt(valor);
        lblRaizQuadrada.setText(String.format("%.2f", raiz));
        //Raiz Cubica
        double raizCubica = Math.cbrt(valor);
        lblRaizCubica.setText(String.format("%.2f", raizCubica));
        //Valor Absoluto
        double valorAbsoluto = Math.abs(valor);
        lblValorAbsoluto.setText(String.format("%.2f", valorAbsoluto));
        
        //Torna o Painel Visivel
        panCalc.setVisible(true);
    }//GEN-LAST:event_btnClickActionPerformed

